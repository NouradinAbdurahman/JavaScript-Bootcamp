/*
    Data types intro
    
    - String
    - Number
    - Array => Object
    - Object => Object
    - Boolean
    - Undefined
    - Null
*/


//String
console.log("Nouraddin Aden");
console.log(`type of Nouraddin Aden is :  ${typeof("Nouradin Aden")}`);

// Number 
console.log(500);
console.log(`type of 500 is :  ${typeof (500)}`);

// Array => Object
console.log(`type of Array 10,12,43,87,55,90 is :  ${typeof [10,12,43,87,55,90]}`);
console.log(`type of Array NOURADDIN , AZZUZ is :  ${typeof ["NOURADDIN", "AZZUZ"]}`);


// Object => Object
console.log(`type of Object name: nouradin, age: 21, country: Somalia is :  ${typeof {name: "NOURADIN", age: 21, country: "Somalia"}}`);


// Boolean
console.log(`type of Boolean true is :  ${typeof true}`);
console.log(`type of Boolean false is :  ${typeof false}`);

// Undefined
console.log(`type of Undefined is :  ${typeof undefined}`);

// Null
console.log(`type of Null is :  ${typeof null}`);
