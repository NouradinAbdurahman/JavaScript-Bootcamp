// Math object

/*
  - round()
  - ceil()
  - floor()
  - min()
  - max()
  - pow()
  - random()
  - trunc() will return the integar number without looking the decimal points 
*/


// round()
console.log(`The Round Number of 9.4 is: ${Math.round(9.4)}`);
console.log(`The Round Number of 9.5 is: ${Math.round(9.5)}`);


// ceil()
console.log(`The ceil Number of 9.4 is: ${Math.ceil(9.4)}`);
console.log(`The ceil Number of 9.5 is: ${Math.ceil(9.8)}`);

// floor()
console.log(`The floor Number of 9.4 is: ${Math.floor(9.4)}`);
console.log(`The floor Number of 9.5 is: ${Math.floor(9.5)}`);



// trunc()
console.log(`The trunc numer of : ${Math.trunc(99.4)}`);

// min()
console.log(`The minimum numer is : ${Math.min(12,32,445,65,4343,21,9)}`);


// max()
console.log(`The maximum numer is : ${Math.max(12,32,445,65,4343,21,9)}`);



// pow()
console.log(`The power numer is : ${Math.pow(12,3)}`);



// rondom()
console.log(`The rondom numer is : ${Math.random()}`);