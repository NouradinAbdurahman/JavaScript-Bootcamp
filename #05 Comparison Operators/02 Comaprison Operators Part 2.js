/*
    Comparison Oparetors
    - ! Not ==> it returns the oppisite thing
    - && And
    - || or
*/

// ! Not
console.log(`true: ${true}`);
console.log(`Not true: ${!true}`);
console.log(!(10 == "10"))

// && And
console.log(10 == "10" && 10 > 5 && 10 >= 10); // in this operator must be true all of them 


// || or
console.log(10 === "10" || 10 > 5 || 10 <= 9); // must one of them to be true 